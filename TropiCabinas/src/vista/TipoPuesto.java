    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (txt_Puesto.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Favor ingresa un tipo de puesto", "Error", JOptionPane.ERROR_MESSAGE);
                txt_Puesto.setText("Aqui");
                return;
            }
            Statement stmt;
            stmt = conexion.createStatement();

            String sql1 = "SELECT `descripcion_puesto` FROM `puesto` WHERE `descripcion_puesto` = '" + txt_Puesto.getText() + "'";
            rs = stmt.executeQuery(sql1);
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Tipo Puesto Existente.", "Error", JOptionPane.ERROR_MESSAGE);
                txt_Puesto.setText("");
                return;
            }
        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);

        }

        Modelo_Puesto dts = new Modelo_Puesto();
        Form_Puesto func = new Form_Puesto();
        
        dts.setDescripcion_puesto(txt_Puesto.getText());
        dts.setPago_hora_sencilla(txt_MontoHS.getText());
        dts.setPago_hora_extra(txt_MontoHE.getText());
        if (func.insertar(dts)) {
            JOptionPane.showMessageDialog(rootPane, "el tipo de puesto creado satisfactoriamente");
        }
        txt_MontoHS.setText("");
        comboTipoPersona();
    }//GEN-LAST:event_btnGuardarActionPerformed

