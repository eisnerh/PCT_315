    private void comboTipoPersona() { //para poder llamarlo desde el otro frame o JDialog
        try {
            modeloTipoPuesto.removeAllElements(); // eliminamos lo elementos
            Statement stmt;
            stmt = conexion.createStatement();
            String queryComboTipoPersona = "SELECT distinct (descripcion_puesto) as puesto FROM pct3.puesto order by descripcion_puesto;";
            rs = stmt.executeQuery(queryComboTipoPersona);
            while (rs.next()) {
                modeloTipoPuesto.addElement(rs.getString("puesto"));
            }
            comboTipoPersona.setModel(modeloTipoPuesto); // seteamos el modelo y se cargan los datos

        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

